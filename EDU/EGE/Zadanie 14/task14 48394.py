''' Найти наименьшее х, при котором а кратно 121 и вычислить а//121 в 10 сс
'''
alph = '0123456789'
for x in alph:
    a = int('4C' + x + '4',15) + int(x + '62A',13)
    if a % 121==0:
        print(x, a//121)

